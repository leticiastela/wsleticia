package repeticao;

import javax.swing.JOptionPane;

public class DesafioDo {
	public static void main(String[] args) {
		float numeroJogador1 = Float.parseFloat(JOptionPane.showInputDialog("Jogador 1 - Digite o número:"));
		float numeroJogador2 = 0;
		int cont = 0;
		do{
			numeroJogador2 = Float.parseFloat(JOptionPane.showInputDialog("Jogador 2 - Digite o número:"));
			if(numeroJogador1 > numeroJogador2)
			{
				JOptionPane.showMessageDialog(null, "O número correto é maior do que o número informado!");
			}
			else if(numeroJogador1 < numeroJogador2)
			{
				JOptionPane.showMessageDialog(null,"O número correto é menor do que o número informado!");
			}
			cont++;
		}
		while(numeroJogador1 != numeroJogador2);
		JOptionPane.showMessageDialog(null,"Parabéns, você acertou com: " + cont + " tentativas!");
	}
}
